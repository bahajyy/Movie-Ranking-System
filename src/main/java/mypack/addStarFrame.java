    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String enteredId=idText.getText();
      String enteredTitle=titleText.getText();
      int enteredYear=Integer.parseInt(yearText.getText());
      String nameSurname=nameSurnameText.getText();
      
       try
        {
            String[] movieIds=getMovieIds();
            int counter=0;
            for(int i=0;i<movieIds.length;i++){
                
                 if((enteredId.equals(movieIds[i]))) {
                    FileWriter Writer = new FileWriter("/Users/bahajyy/Desktop/java/Proje/2224PROJECT/Stars.txt",true);
                    Writer.write(enteredId + " "+ enteredTitle + " "+ enteredYear+" "+nameSurname+" "+"\n");
                    Writer.close();
                    JOptionPane.showMessageDialog(null,"Star added to the file");
                    setVisible(false);
                    new addStarFrame().setVisible(true);
                }
                 else 
                {
                    JOptionPane.showMessageDialog(null,"This movie is not in Movies File!");                   
                    setVisible(false);
                    new addStarFrame().setVisible(true);
                }
                 counter++;
            }
                  
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error");
        }
      
        
    }//GEN-LAST:event_jButton1ActionPerformed

