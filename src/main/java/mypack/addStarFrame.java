      public static String[] getMovieIds()
    {
        File MovieFile = new File("/Users/bahajyy/Desktop/java/Proje/2224PROJECT/Movies.txt");
        String getId;
        
        String[] movieIds=new String[5];
                
        
        try {        
            
            BufferedReader read = new BufferedReader(new FileReader(MovieFile));
            String line;
            int i=0;       
            while((line = read.readLine()) != null)
            {             
                String[] movieInfo = line.split(" ");
                getId = movieInfo[0];
                movieIds[i]=getId;
                i++;                                 
            }                      
            read.close();                
        }   
        catch (FileNotFoundException ex) {
            System.out.println("File is not found");
        } 
        catch (IOException ex) {
             System.out.println("IO Exception");
        } 
        
        for(int i=0;i<movieIds.length;i++){
               System.out.println(movieIds[i]);
            }
        
        return movieIds;
    }
    
    

    
